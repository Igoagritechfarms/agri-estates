const defaultImage = fallbackProperties[0].image;
const hasImage = (value?: string) => Boolean(value && value.trim());

export const normalizeProperty = (property: Partial<Property>, index = 0): Property => {
  const fallback = fallbackProperties[index % fallbackProperties.length];

  return {
    id: String(property.id || fallback.id),
    created_at: property.created_at || fallback.created_at,
    title: property.title || fallback.title,
    location: property.location || fallback.location,
    size: property.size || fallback.size,
    price: property.price || fallback.price,
    roi: property.roi || fallback.roi,
    image: hasImage(property.image) ? property.image! : fallback.image || defaultImage,
    status: property.status || 'Available',
    type: property.type || fallback.type,
    description: property.description || fallback.description,
    features: property.features || fallback.features || [],
    projectUrl: property.projectUrl || fallback.projectUrl,
    projectAddress: property.projectAddress || fallback.projectAddress,
    projectOffice: property.projectOffice || fallback.projectOffice,
    crops: property.crops || fallback.crops,
    setupScope: property.setupScope || fallback.setupScope,
    revenueModel: property.revenueModel || fallback.revenueModel,
    costRange: property.costRange || fallback.costRange,
    breakEven: property.breakEven || fallback.breakEven,
    customerNeeds: property.customerNeeds || fallback.customerNeeds,
    igoSupport: property.igoSupport || fallback.igoSupport,
    reraNumber: property.reraNumber,
    totalUnits: property.totalUnits,
    availableUnits: property.availableUnits,
    images: property.images?.filter(hasImage),
    brochureUrl: property.brochureUrl,
    soldAt: property.soldAt,
    bookedAt: property.bookedAt,
  };
};
